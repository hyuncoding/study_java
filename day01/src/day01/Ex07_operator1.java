package day01;

public class Ex07_operator1 {
	public static void main(String[] args) {
		
		/*
		 * 1. 산술 연산자
		 * 		1) +
		 * 		2) - 
		 * 		3) *
		 * 		4) / : 몫 반환
		 * 		5) % : 나머지 반환
		 * 
		 * 2. 대입 연산자
		 * 		1) = : 대입
		 * 		2) += : 더해서 대입
		 * 		3) -= : 빼서 대입
		 * 		4) *= : 곱해서 대입
		 * 		5) /= : 몫을 구해서 대입
		 * 		6) %= : 나머지를 구해서 대입
		 * 
		 * 3. 증감 연산자
		 * 		1) a++ : a를 사용하고 1 증가
		 * 		2) ++a : a를 1 증가시켜서 사용
		 * 		3) a-- : a를 사용하고 1 감소
		 * 		4) --a : a를 1 감소시켜서 사용
		 * 
		 */
		
		int a = 1;
		int b = 1;
		
		System.out.println(a++);
		System.out.println(a);
		System.out.println("==========");
		System.out.println(++b);
		System.out.println(b);
		
		
	}
}
